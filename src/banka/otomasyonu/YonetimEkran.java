    private void resetColor(JPanel[] pane, JPanel[] ind) {
        for (int i = 0; i < pane.length; i++) {
            pane[i].setBackground(new Color(23, 35, 51));
        }
        for (int i = 0; i < ind.length; i++) {
            ind[i].setOpaque(false);
        }
    }
    private void personelleriEkle(){
        try {
            DefaultTableModel model = (DefaultTableModel) PersonellerTablo.getModel();
            model.setRowCount(0);
            FileReader okuyucu = new FileReader("PersonelBilgi.txt");
            BufferedReader br = new BufferedReader(okuyucu);
            String satır;
            while ((satır = br.readLine()) != null) {
                String bilgiler[] = new String[7];
                bilgiler = satır.split("/");
                model.addRow(new Object[]{bilgiler[0],bilgiler[1], bilgiler[2], bilgiler[3], bilgiler[5]});
            }
            okuyucu.close();
            br.close();
        } catch (Exception e) {
            
        }
    }
