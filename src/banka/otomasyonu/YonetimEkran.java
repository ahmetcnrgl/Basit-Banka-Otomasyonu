    public YonetimEkran(String TC, String sifre) {
        try {
            FileReader okuyucu = new FileReader("YöneticiBilgi.txt");
            BufferedReader br = new BufferedReader(okuyucu);
            String satır;
            while ((satır = br.readLine()) != null) {
                String bilgiler[] = new String[7];
                bilgiler = satır.split("/");
                if (TC.equals(bilgiler[2]) && sifre.equals(bilgiler[4])) {
                    yeniY = new Yonetici(bilgiler[0], bilgiler[1], TC, bilgiler[3], sifre, bilgiler[5]);
                }
            }
            okuyucu.close();
            br.close();
        } catch (Exception e) {
            
        }
        initComponents();
        setColor(GirisEkranıPanel);
        GirisEkranıPanel.setOpaque(true);
        resetColor(
            new JPanel[]{PersonelPanel,CikissYapPanel},
            new JPanel[]{PersonelPanel,CikissYapPanel}
        );
        HosgeldinizEkran.setVisible(true);
        PersonelEkran.setVisible(false);
        setLocationRelativeTo(null);
        setResizable(false);
    }

