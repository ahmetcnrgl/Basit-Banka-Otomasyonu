    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int row;
        DefaultTableModel model = (DefaultTableModel) PersonellerTablo.getModel();
        if (-1 == PersonellerTablo.getSelectedRow()) {
            JOptionPane.showMessageDialog(this, "Lütfen işten çıkarmak istediğiniz personeli seçiniz", "Hata", JOptionPane.WARNING_MESSAGE);
            return;
        }
        else{
            row = PersonellerTablo.getSelectedRow();
        }
        String TC = model.getValueAt(row, 2).toString();
        yeniY.personelIstenCıkar(TC);
        personelleriEkle();
    }//GEN-LAST:event_jButton1ActionPerformed

